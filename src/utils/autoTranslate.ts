// Function to merge base translations with auto translations
export function getCompleteTranslations(languageCode: string): LanguageTranslations {
  // If English, return complete English translations
  if (languageCode === 'en') {
    return englishTranslations;
  }

  const languageTranslations = autoTranslations[languageCode];

  if (!languageTranslations) {
    return englishTranslations; // Fallback to English
  }

  // Deep merge translations with English as fallback
  return {
    ...englishTranslations,
    ...languageTranslations,
    welcome: {
      ...englishTranslations.welcome,
      ...(languageTranslations.welcome || {}),
      features: {
        ...englishTranslations.welcome.features,
        ...(languageTranslations.welcome?.features || {}),
      },
    },
    termsPrivacy: {
      ...englishTranslations.termsPrivacy,
      ...(languageTranslations.termsPrivacy || {}),
    },
    userDetails: {
      ...englishTranslations.userDetails,
      ...(languageTranslations.userDetails || {}),
    },
    signIn: {
      ...englishTranslations.signIn,
      ...(languageTranslations.signIn || {}),
    },
    phoneInput: {
      ...englishTranslations.phoneInput,
      ...(languageTranslations.phoneInput || {}),
    },
    common: {
      ...englishTranslations.common,
      ...(languageTranslations.common || {}),
    },
  };
}